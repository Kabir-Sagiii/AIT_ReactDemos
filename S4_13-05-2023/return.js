function addParams(x, y) {
  var a;
  var b;

  a = x;
  b = y;

  var result = a + b;

  return result;
}

var firstResult = addParams(100, 200);
console.log(firstResult);

var secondResult = addParams(50, 50);
console.log(secondResult);

var thirdResult = addParams(30, 50);
console.log(thirdResult);
